public class Tipo{
    private int codigo;
    private double percentual;

    public Tipo(){

    }
    public void setCodigo(int c){
        codigo = c;
    }
    public int getCodigo(){
        return codigo;
    }
    public void setPercentual(double p){
        percentual = p;
    }
    public double getPercentual(){
        return percentual;
    }
}